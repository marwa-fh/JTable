    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String batchname = jtxtBatch.getText();
       String year = jtxtYear.getText();
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
              con =DriverManager.getConnection("jdbc:sqlserver://localhost:1433;databaseName=testjava;integratedSecurity=true;");
              
              
              pst = con.prepareStatement("insert into batch(banchname,year)values(?,?)");
               pst.setString(1, batchname);
                pst.setString(2, year);
                
                 pst.executeUpdate();
                 JOptionPane.showMessageDialog(null, " Batch Created.....");
                 jtxtBatch.setText("");
                 jtxtYear.setText("");
                 jtxtBatch.requestFocus();     ;
                
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Batch.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Batch.class.getName()).log(Level.SEVERE, null, ex);
        }
               
              
    }//GEN-LAST:event_jButton1ActionPerformed

